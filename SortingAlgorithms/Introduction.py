# WHAT IS SORTING?

# By definition sorting refers to the arranging of data in a particular format: either ascending or descending.
# Practical Examples: Sorting Function in Excel, Online Shopping (Sorting by price, customer review etc.)

# What are the types of sorting algorithms?

# Can be categorized into two categories. 
# 1. Space Used
#   a. In Pace: Sorting algorithms which do not require extra space for sorting. Eg: Bubble Sort
#   b. Out of Pace: Extra space is required for sorting: Eg. Merge Sort
# 2. Stability
#   a. Stable: If sorting algorithm after sorting does not change the sequence of similar content in which they appear, then this sorting is called stable sorting. Eg. Insertion Sort
#   b. Unstable: If a sorting algorithm changes the sequence of similar data in which they appear then this sorting is called unstable sorting. Eg. Quick Sort

# Sorting Terminology
# Increasing Order: If successive elements are greater than the previous one
# Eg. 1, 3, 5, 7, 9, 11
# Decreasing Order: If successive elements are lesser than the previous one
# Eg. 11, 9, 7, 5, 3, 1
# Non Increasing Order: If successive elements is less than or equal to the previous element in the sequence.
# Eg. 11, 9, 7, 5, 5, 3, 1
# Non decreasing Order: If successive elements is greater  than or equal to the previous element in the sequence.
# Eg. 11, 9, 7, 5, 5, 3, 1

# Sorting Algorithms:
# 1. Bubble Sort
# 2. Selection Sort
# 3. Insertion Sort
# 4. Bucket Sort
# 5. Merge Sort
# 6. Quick Sort
# 7. Heap Sort

# Which one to select? Depends on stability, space and time complexity 